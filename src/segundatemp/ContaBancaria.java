package segundatemp;

public class ContaBancaria {
	String agencia;
	double saldo;
	int conta;
	Pessoa pessoa;
	
	public ContaBancaria(String agencia, double saldo, int conta, Pessoa pessoa) {
		super();
		this.agencia = agencia;
		this.saldo = saldo;
		this.conta = conta;
		this.pessoa = pessoa;
	}
	
		
	

}
